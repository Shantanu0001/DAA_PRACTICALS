def evenodd(n):
    if n%2==0:
        print(n," is an even number.")
    else:
        print(n," is not an even number.")

def pos_neg_zero(n):
    if n==0:
        print("It is zero.")
    elif n<0:
        print("It is negative number.")
    else:
        print("It is positive number.")

evenodd(54)
pos_neg_zero(12)